package com.appslab;

public class Student {
    String name;
    int studentnumber;
    String address;
    int phonenumber;

    public String getName() {
        return name;
    }

    public int getStudentnumber() {
        return studentnumber;
    }

    public String getAddress() {
        return address;
    }

    public int getPhonenumber() {
        return phonenumber;
    }
}
